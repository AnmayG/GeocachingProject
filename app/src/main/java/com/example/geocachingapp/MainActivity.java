    private boolean notificationExists = false;

    private void sendNotification(String content) {
        String NOTIFICATION_CHANNEL_ID = "geocache_nearby";
        NotificationManager notificationManager = (NotificationManager) getSystemService(Context.NOTIFICATION_SERVICE);

        if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.M) {
            for (StatusBarNotification s:notificationManager.getActiveNotifications()) {
                if(s.getId() == 1) {
                    notificationExists = true;
                    return;
                } else {
                    notificationExists = false;
                }
            }
        }

        if(Build.VERSION.SDK_INT>=Build.VERSION_CODES.O && !notificationExists){
            NotificationChannel notificationChannel = new NotificationChannel(NOTIFICATION_CHANNEL_ID, "My notifications", NotificationManager.IMPORTANCE_DEFAULT);
            notificationChannel.setDescription("Channel description");
            notificationChannel.enableLights(true);
            notificationChannel.setLightColor(Color.RED);
            notificationChannel.setVibrationPattern(new long[] {0, 1000, 500, 1000});
            notificationChannel.enableVibration(true);
            notificationManager.createNotificationChannel(notificationChannel);
        }
        NotificationCompat.Builder builder = new NotificationCompat.Builder(this, NOTIFICATION_CHANNEL_ID);
        builder.setContentTitle("Geocache Nearby!")
                .setContentText(content)
                .setAutoCancel(false)
                .setSmallIcon(R.drawable.logo)
                .setLargeIcon(BitmapFactory.decodeResource(getResources(), R.mipmap.ic_launcher));
        Notification notification = builder.build();
        notificationManager.notify(1, notification);
    }

    private List<QRCode> readQRCodesFromCSV() {
        List<QRCode> qrCodes = new ArrayList<>();
        int row = 0;
        try {
            InputStream csvfile = getAssets().open("worldcities.csv");
            CSVReader reader = new CSVReader(new InputStreamReader(csvfile));
            String[] nextLine;
            while ((nextLine = reader.readNext()) != null && row <= 25) {
                row++;
                if(row == 1) continue;
                String hash = QrMakeFragment.generateVerificationKey(QrMakeFragment.getSaltString());
                Location l = new Location("city");
                l.setLatitude(Double.parseDouble(nextLine[2]));
                l.setLongitude(Double.parseDouble(nextLine[3]));

                QRCode qrcode = new QRCode(hash, nextLine[1], nextLine[1] + ", " + nextLine[4], null, Double.parseDouble(nextLine[2]),
                        Double.parseDouble(nextLine[3]),
                        null,
                        getAddress(l)
                );

                Log.d(TAG, qrcode.toString());
                // adding qrcode into ArrayList
                qrCodes.add(qrcode);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return qrCodes;
    }

    public String getAddress(Location l) {
        Geocoder geocoder;
        List<Address> addresses;
        geocoder = new Geocoder(getApplicationContext(), Locale.getDefault());
        String addressThing = "";
        try {
            addresses = geocoder.getFromLocation(l.getLatitude(), l.getLongitude(), 1);
            addressThing = addresses.get(0).getAddressLine(0);
        } catch (Exception e) {
            e.printStackTrace();
        }
        return addressThing;
    }

